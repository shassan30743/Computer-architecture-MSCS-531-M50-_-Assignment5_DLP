import numpy as np

# Initialize two vectors for addition
vector1 = np.array([1, 2, 3, 4, 5, 6, 7, 8])
vector2 = np.array([8, 7, 6, 5, 4, 3, 2, 1])

# Perform vector addition
result = np.add(vector1, vector2)

print("Vector 1:", vector1)
print("Vector 2:", vector2)
print("Result of vector addition:", result)
